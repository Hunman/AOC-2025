#include "day7/tachyon_manifold.hpp"
#include "gtest/gtest.h"

TEST(Day7Test, GivenExercise1Test) {
    TachyonManifold manifold({
        ".......S.......",
        "...............",
        ".......^.......",
        "...............",
        "......^.^......",
        "...............",
        ".....^.^.^.....",
        "...............",
        "....^.^...^....",
        "...............",
        "...^.^...^.^...",
        "...............",
        "..^...^.....^..",
        "...............",
        ".^.^.^.^.^...^.",
        "...............",
    });

    ASSERT_EQ(21, manifold.exercise1());
}

