#include "day7/tachyon_manifold.hpp"
#include "gtest/gtest.h"

TEST(Day7Test, GivenExercise1Test) {
    std::vector<std::string> manifold({
        ".......S.......",
        "...............",
        ".......^.......",
        "...............",
        "......^.^......",
        "...............",
        ".....^.^.^.....",
        "...............",
        "....^.^...^....",
        "...............",
        "...^.^...^.^...",
        "...............",
        "..^...^.....^..",
        "...............",
        ".^.^.^.^.^...^.",
        "...............",
    });

    ASSERT_EQ(21, TachyonManifold::exercise1(manifold));
}

